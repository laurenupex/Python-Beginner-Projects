from math import sqrt
# imports square root function from math package
from math import factorial

x = 15 * 3
y = 200
# values assigned to x and y variables
print(x, y)
# prints variables x and y

number = 4
print(number**0.5)
# long way to calculate square root
print(sqrt(number))
# shorter way to calculate square root using function from math package
# 5! = 5 x 4 x 3 x 2 x 1 (factorial)
print(factorial(5))
